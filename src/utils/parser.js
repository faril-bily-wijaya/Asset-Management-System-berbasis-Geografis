export function parseInventoryCSV(text) {
  const parts = text.split(';');
  const records = [];
  let i = 0;
  
  // skip headers
  if (parts[0] === 'DEVICE_CODE' || parts[0].includes('DEVICE_CODE')) {
    i = 10;
  }

  while (i < parts.length) {
    if (!parts[i]) {
      i++;
      continue;
    }
    
    // Potential DEVICE_CODE
    const record = {
      DEVICE_CODE: parts[i],
      DEVICE_TYPE: parts[i+1],
      BRAND: parts[i+2],
      CONDITION: parts[i+3],
      STATUS: parts[i+4],
      CAP_REAL: parts[i+5],
      CAP_STATUS: parts[i+6],
      LOCATION: parts[i+7],
      ROOM: parts[i+8],
      CODE: parts[i+9],
    };
    
    let j = i + 10;
    while (j < parts.length && parts[j] === '') {
      j++;
    }
    
    if (parts[j] && (parts[j].startsWith('R1.') || parts[j].length < 15 && !parts[j].includes('-') && !parts[j].match(/^[0-9]+$/))) {
      i = j;
    } else {
      record.UID = parts[j];
      i = j + 1;
    }
    
    if (record.DEVICE_CODE && record.LOCATION && record.DEVICE_TYPE) {
      records.push(record);
    }
  }
  
  return records;
}

export function isCatuDaya(deviceType) {
  if (!deviceType) return false;
  const type = deviceType.toUpperCase();
  const catuDayaKeywords = ['GENSET', 'BATTERE', 'RECTIFIER', 'MDP', 'ATS', 'TANGKI', 'PDB', 'AVR', 'INVERTER', 'UPS'];
  return catuDayaKeywords.some(keyword => type.includes(keyword));
}
